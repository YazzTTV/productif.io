export function LanguageProvider({ children, initialLocale = 'fr' }: { children: ReactNode, initialLocale?: Locale }) {
  const [locale, setLocaleState] = useState<Locale>(initialLocale);
  const [mounted, setMounted] = useState(false);

  // Charger la langue depuis AsyncStorage au d√©marrage
  useEffect(() => {
    const loadLocale = async () => {
      try {
        const savedLocale = await AsyncStorage.getItem(LOCALE_STORAGE_KEY);
        if (savedLocale && (savedLocale === 'fr' || savedLocale === 'en')) {
          setLocaleState(savedLocale as Locale);
        }
      } catch (error) {
        console.error('Erreur lors du chargement de la langue:', error);
      } finally {
        setMounted(true);
      }
    };
    loadLocale();
  }, []);

  // Sauvegarder la langue dans AsyncStorage √† chaque changement
  useEffect(() => {
    if (mounted) {
      AsyncStorage.setItem(LOCALE_STORAGE_KEY, locale).catch((error) => {
        console.error('Erreur lors de la sauvegarde de la langue:', error);
      });
    }
  }, [locale, mounted]);

  // Fonction de traduction
  const t = (key: string): string => {
    if (locale === 'fr' && key in translations.fr) {
      return translations.fr[key as keyof typeof translations.fr];
    } else if (locale === 'en' && key in translations.en) {
      return translations.en[key as keyof typeof translations.en];
    }
    return key;
  };

  const setLocale = (newLocale: Locale) => {
    setLocaleState(newLocale);
  };

  return (
    <LanguageContext.Provider value={{ locale, setLocale, t }}>
      {children}
    </LanguageContext.Provider>
  );
}

export function useLanguage(): LanguageContextType {
  const context = useContext(LanguageContext);
  if (context === undefined) {
    // Retourner des valeurs par d√©faut au lieu de lancer une erreur
    // Cela permet au composant de fonctionner m√™me si le provider n'est pas encore mont√©
    console.warn('useLanguage called outside LanguageProvider, using defaults');
    return {
      locale: 'fr',
      setLocale: () => {},
      t: (key: string) => key,
    };
  }
  return context;
}

